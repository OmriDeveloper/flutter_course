class DoubleExample {

  // This method shows examples of declaring and printing double variables
  void doubleExamples() {
    // Temperature in Celsius
    double temperature = 36.6;
    print('Temperature: $temperature');

    // Product price
    double price = 19.99;
    print('Price: $price');

    // Height in meters
    double height = 1.75;
    print('Height: $height');

    // Weight in kilograms
    double weight = 68.5;
    print('Weight: $weight');

    // Average score
    double averageScore = 87.3;
    print('Average Score: $averageScore');

    // Distance in kilometers
    double distance = 12.4;
    print('Distance: $distance');

    // Speed in km/h
    double speed = 80.5;
    print('Speed: $speed');

    // Bank account balance
    double balance = 1234.56;
    print('Balance: $balance');

    // Rating out of 5
    double rating = 4.8;
    print('Rating: $rating');

    // Interest rate
    double interestRate = 2.5;
    print('Interest Rate: $interestRate');
  }

  // This method demonstrates basic operations with double variables
  void doubleOperations() {
    // Create a double variable
    double number = 10.5;

    // Print the original value
    print('Original number: $number');

    // Add 4.5
    number = number + 4.5;
    print('After adding 4.5: $number');

    // Subtract 2.0
    number = number - 2.0;
    print('After subtracting 2.0: $number');

    // Multiply by 3
    number = number * 3;
    print('After multiplying by 3: $number');

    // Divide by 2
    number = number / 2;
    print('After dividing by 2: $number');
  }

}
