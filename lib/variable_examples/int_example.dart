class IntExample {

  // This method shows examples of declaring and printing integer variables
  void intExamples() {
    // Declare age
    int age = 18;
    print('Age: $age');

    // Declare a year
    int year = 2025;
    print('Year: $year');

    // Number of days in a week
    int daysInWeek = 7;
    print('Days in a week: $daysInWeek');

    // Temperature value
    int temperature = 30;
    print('Temperature: $temperature');

    // A score in a game/test
    int score = 100;
    print('Score: $score');

    // Count of items in a list or cart
    int itemsCount = 15;
    print('Items count: $itemsCount');

    // Number of minutes in an hour
    int minutes = 60;
    print('Minutes: $minutes');

    // Number of hours in a day
    int hours = 24;
    print('Hours: $hours');

    // City or country population
    int population = 500000;
    print('Population: $population');

    // Speed of a car
    int speed = 90;
    print('Speed: $speed');

    // Game or app level
    int level = 3;
    print('Level: $level');

    // Number of attempts or tries
    int attempts = 0;
    print('Attempts: $attempts');

    // Height in centimeters
    int heightCm = 170;
    print('Height in cm: $heightCm');

    // Weight in kilograms
    int weightKg = 65;
    print('Weight in kg: $weightKg');

    // Number of apples
    int apples = 4;
    print('Apples: $apples');

    // Number of bananas
    int bananas = 6;
    print('Bananas: $bananas');

    // Number of books
    int books = 12;
    print('Books: $books');

    // Number of chairs
    int chairs = 8;
    print('Chairs: $chairs');

    // Number of tables
    int tables = 2;
    print('Tables: $tables');

    // Number of rooms
    int rooms = 5;
    print('Rooms: $rooms');

    // Number of floors in a building
    int floors = 10;
    print('Floors: $floors');

    // Number of tickets available
    int tickets = 50;
    print('Tickets: $tickets');

    // Price of an item
    int price = 120;
    print('Price: $price');

    // Discount amount
    int discount = 10;
    print('Discount: $discount');

    // Final score after calculation
    int finalScore = 95;
    print('Final Score: $finalScore');
  }

  // This method demonstrates basic arithmetic operations with integers
  void intCalculations() {
    // Create an integer variable
    int number = 10;

    // Print the initial value
    print('The number is: $number');

    // Add 5 to the number
    number = number + 5;
    print('After adding 5: $number');

    // Subtract 2 from the number
    number = number - 2;
    print('After subtracting 2: $number');

    // Multiply the number by 3
    number = number * 3;
    print('After multiplying by 3: $number');
  }

}